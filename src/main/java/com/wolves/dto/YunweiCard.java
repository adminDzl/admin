package com.wolves.dto;

public class YunweiCard {
	private String name;
	private String sex;
	private String card;
	private String phone;
	private String business_type;
	private String limits;
	private String special_case_supplement;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getCard() {
		return card;
	}
	public void setCard(String card) {
		this.card = card;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getBusiness_type() {
		return business_type;
	}
	public void setBusiness_type(String business_type) {
		this.business_type = business_type;
	}
	public String getLimits() {
		return limits;
	}
	public void setLimits(String limits) {
		this.limits = limits;
	}
	public String getSpecial_case_supplement() {
		return special_case_supplement;
	}
	public void setSpecial_case_supplement(String special_case_supplement) {
		this.special_case_supplement = special_case_supplement;
	}
	

}
